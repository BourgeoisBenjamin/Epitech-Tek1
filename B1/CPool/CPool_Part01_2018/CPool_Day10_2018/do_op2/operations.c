int my_addition(int n1, int n2)
{
    return (n1 + n2);
}

int my_soustraction(int n1, int n2)
{
    return (n1 - n2);
}

int my_multiplication(int n1, int n2)
{
    return (n1 * n2);
}

int my_division(int n1, int n2)
{
    return (n1 / n2);
}

int my_modulo(int n1, int n2)
{
    return (n1 % n2);
}